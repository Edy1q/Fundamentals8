
package lession11.arrays;

public class TablasMultiplicar {
    public static void main(String[] args) {
       
        for(int i=0; i<=10; i++){
            for(int y=0; y<=10; y++){
                System.out.println(i+"*"+y+"="+i*y);
            }
               System.out.println("\n"); 
        }
    }
}
